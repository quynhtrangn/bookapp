    private void jLabel_BsigninMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_BsigninMouseClicked
        UserDAO userDAO = new UserDAO();
        String username = jTextField_User.getText();
        String password = jPasswordField_Pass.getText();
        UserModel usermodel = userDAO.selectUser(username);
        if(username.equals("")){
            JOptionPane.showMessageDialog(this, "Enter username");
            return;
        }
        if(password.equals("")){
            JOptionPane.showMessageDialog(this, "Enter password");
            return;
        }
        if(usermodel == null){
            JOptionPane.showMessageDialog(this,"Account not exist");
            return;
        }
        if(usermodel.getPassword().equals(password)){
            Dashboard_Form dashboard = new Dashboard_Form();
            dashboard.setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this, "Account not exist");
            return;
        }
    }//GEN-LAST:event_jLabel_BsigninMouseClicked

