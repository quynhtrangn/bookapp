    private void jLabel_bsignupMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_bsignupMouseClicked
        // TODO add your handling code here:
         try {
        UserDAO userDAO = new UserDAO();
        String username = jTextField_addname.getText();
        String password = jPasswordField_addPass.getText();
        String email = jTextField_mail.getText();
        if(username.equals("")){
            JOptionPane.showMessageDialog(this, "Lack of username");
            return;
        }
        
        if(password.equals("")){
            JOptionPane.showMessageDialog(this, "Lack of password");
            return;
        }
        if(email.equals("")){
            JOptionPane.showMessageDialog(this, "Lack of mail");
            return;
        }
        if(userDAO.selectUser(username)!=null ){
            JOptionPane.showMessageDialog(this,"This account existed");
            return;
        }
        UserModel userModel= new UserModel(userDAO.selectAllUsers().size()+1,username,password,email);
        userDAO.insertUser(userModel);
        JOptionPane.showMessageDialog(this,"Succeed");  
             showPanels(jPanel_TabLogin);
        } catch (SQLException ex) {
            Logger.getLogger(Login_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jLabel_bsignupMouseClicked

